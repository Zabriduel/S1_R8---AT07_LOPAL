for (contador = 10; contador >= 1; contador--) {
    console.log(`Numero: ${contador}`);
}
console.log("Fim");

console.log("While");
let contadorWhile = 10;
while (contadorWhile >= 1) {
    console.log(`Numero: ${contadorWhile}`);
    contadorWhile --;
}
console.log("FIM");

console.log("Do");
let contadorDo = 10;
do {
    console.log(`Numero: ${contadorDo}`);
    contadorDo -- ;
} while (contadorDo >= 1) ;
console.log("FIM");
